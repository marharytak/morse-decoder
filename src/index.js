const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    return expr.match(/.{10}/g).map(letter => {
        letter = letter.replace(/10/g, '.').replace(/11/g, '-').replace(/0/g, '');

        if (letter.match(/\*{10}/g)) {
            return ' ';
        }

        for (var key in MORSE_TABLE) {
            if (letter === key) {
                return letter.replace(key, MORSE_TABLE[key]);
            }
        }
    }).join('');
}

module.exports = {
    decode
}